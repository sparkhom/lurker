// Regenerates vue_client/src/utils/emojiData.ts — the `:shortcode:` → emoji
// table behind Slack-style emoji entry. Run after a gemoji release to pick up
// new emoji:
//
//   node tools/gen-emoji-data.mjs
//
// Source: github/gemoji db/emoji.json, the de-facto shortcode set shared by
// GitHub, Slack and Discord. Each emoji's `aliases` become shortcode keys.

import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const SRC = 'https://raw.githubusercontent.com/github/gemoji/master/db/emoji.json';
const here = dirname(fileURLToPath(import.meta.url));
const OUT = join(here, '..', 'vue_client', 'src', 'utils', 'emojiData.ts');

const res = await fetch(SRC);
if (!res.ok) throw new Error(`fetch ${SRC} failed: ${res.status}`);
const data = await res.json();

/** @type {Record<string, string>} */
const map = {};
for (const e of data) {
  for (const alias of e.aliases || []) map[alias] = e.emoji;
}
const names = Object.keys(map).toSorted();
const entries = names.map((n) => `  ${JSON.stringify(n)}: ${JSON.stringify(map[n])},`).join('\n');

const file = `// Copyright (c) 2026 Brad Root
// SPDX-License-Identifier: MPL-2.0

// AUTO-GENERATED by tools/gen-emoji-data.mjs — do not edit by hand.
//
// Source: github/gemoji db/emoji.json — the de-facto \`:shortcode:\` set
// shared by GitHub, Slack and Discord. ${data.length} emoji, ${names.length} shortcode aliases.
//
// This module is deliberately a separate chunk: it is dynamically imported
// only when the user first types a \`:\` (see \`loadEmoji\` in
// emojiShortcodes.ts), so its weight never lands in the initial bundle.

import { rankShortcodes } from './emojiShortcodes.js';

// Shortcode body (no surrounding colons) → emoji glyph.
export const EMOJI: Record<string, string> = {
${entries}
};

const NAMES = Object.keys(EMOJI);

export interface EmojiMatch {
  /** Shortcode body, no colons. */
  name: string;
  /** The emoji glyph. */
  emoji: string;
}

// Ranked shortcode matches for the suggester strip.
export function searchEmoji(query: string, limit = 30): EmojiMatch[] {
  return rankShortcodes(NAMES, query)
    .slice(0, limit)
    .map((name) => ({ name, emoji: EMOJI[name] }));
}

// Resolve a single shortcode (case-insensitive) to its emoji, or null if it
// is not a known shortcode.
export function emojiForShortcode(name: string): string | null {
  return EMOJI[name.toLowerCase()] ?? null;
}
`;

writeFileSync(OUT, file);
console.log(`wrote ${names.length} shortcodes to ${OUT}`);
